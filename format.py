# Процентное форматирование
name = 'Vi'
age = 24
print('Your name %s, your age %d.' %(name, age))

# Форматирование .format
name = 'Vi'
age = 24
print('Your name {}, your age {}.' .format(name, age))

# Форматирование f-строка
name = 'Vi'
age = 24
print(f'Your name {name}, your age {age}.')

# точка в данном случае обозначает дробное число, 4 - количество знаков после запятой, а f - float
print(f'{10/7:.4f}')


